									<h3>FoodRubix</h3>

									<p>This is the application I enjoyed doing the most because I had to create everything from scratch and I was working in <strong>Agile environment</strong> with my client to offer her the best value.</p>
									<p>I also had to look into all aspects of a web projects, sketching, coding, designing, testing, and optimizing performances to nake the app.</p>
